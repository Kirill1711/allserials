function addactive (items) {
    for(let i = 0; i < items.length; i++) {
        items[i].addEventListener('click', function (){
            for(let j = 0; j < items.length; j++) {
                items[j].classList.remove('active__filter');
            }
            items[i].classList.add('active__filter');
        });
    }
}
